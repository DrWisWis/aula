let fruit = "kiwi"

switch (fruit) {
    case "maca":
        console.log(`${fruit} = R$ 5,00`)
        break;

        case "banana":
            console.log(`${fruit} = R$ 7,00`)
            break;

            case "pera":
                console.log(`${fruit} = R$ 4,00`)
                break;

                case "laranja":
                    console.log(`${fruit} = R$ 9,00`)
                    break;

                    case "kiwi":
                        console.log(`${fruit} = R$ 25,00`)
                        break;

    default:
        break;
}